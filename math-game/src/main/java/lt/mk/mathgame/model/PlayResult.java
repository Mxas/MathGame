package lt.mk.mathgame.model;

public class PlayResult {
    private int total;
    private int correct;


    public int getTotal() {
        return total;
    }

    public int getCorrect() {
        return correct;
    }

    public void setCorrect(int correct) {
        this.correct = correct;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
